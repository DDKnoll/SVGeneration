SVGeneration.Navbar = React.createClass({
  componentDidMount: function() {
    $.ajax({
        url: '/recipes/data.json',
        type: 'get',
        success: function (data) {
          this.setState({graphics: data});
        }.bind(this)
      });
  },
  getInitialState: function() {
    return {
      graphics:[],
      menu: false
    };
  },
  toggleMenu: function(){
    this.setState({menu : !this.state.menu});
  },
  render: function() {
    return (
      <nav id="navbar">
        <a className='logo-link' class='title-link' href="/">
          <SVGeneration.Icons.Logo />
          <div className="title">SVGeneration</div>
        </a>
        <a onClick={this.toggleMenu} className='browse-all-button'>
          <SVGeneration.Icons.Squares />
        </a>
        <SVGeneration.GraphicList graphics={this.state.graphics} active={this.state.menu}/>
      </nav>
    );
  }
});

SVGeneration.GraphicList = React.createClass({
  getDefaultProps: function() {
    return {
      graphics: []
    };
  },
  render: function() {
    return (
      <div className={this.props.active ? "active menu-dropdown": "menu-dropdown"}>
        <ul>
          {
            this.props.graphics.map(function(img){
              return (
                <li>
                  <a href={"/recipes/"+img}>{img}</a>
                </li>
              );
            })
          }
        </ul>
      </div>
    );
  }
});

